package space_colonization;

public class Battle_Object {
	private double hp;
	public Battle_Object()
	{
		hp = 10;
	}
	public Battle_Object(double HP)
	{
		hp = HP;
	}
	public double getHP()
	{
		return hp;
	}
	public void changeHP(double HP)
	{
		hp = HP;
	}
	public void setHP(double HP)
	{
		hp = HP;
	}
	

}
